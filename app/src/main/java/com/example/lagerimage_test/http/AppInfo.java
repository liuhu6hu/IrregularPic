package com.example.lagerimage_test.http;

public class AppInfo {
    private String support;
    private String supportTel;
    private String licenseInfo;
    private String licenseExpireInfo;
    private String about;

    public String getSupport() {
        return support;
    }

    public void setSupport(String support) {
        this.support = support;
    }

    public String getSupportTel() {
        return supportTel;
    }

    public void setSupportTel(String supportTel) {
        this.supportTel = supportTel;
    }

    public String getLicenseInfo() {
        return licenseInfo;
    }

    public void setLicenseInfo(String licenseInfo) {
        this.licenseInfo = licenseInfo;
    }

    public String getLicenseExpireInfo() {
        return licenseExpireInfo;
    }

    public void setLicenseExpireInfo(String licenseExpireInfo) {
        this.licenseExpireInfo = licenseExpireInfo;
    }

    public String getAbout() {
        return about;
    }

    public void setAbout(String about) {
        this.about = about;
    }
}
